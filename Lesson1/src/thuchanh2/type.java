package thuchanh2;
public class type {
    public static void main(String[] args) {
        int i = 10;
        float f = 20.5f;
        double d = 20.5d;
        boolean b = true;
        char c = 'a';
        String s = "Hà Nội";
        System.out.println("Biến i kiểu số nguyên, có giá trị là " + i);
        System.out.println("Biến f kiểu số thực, có giá trị là " + f);
        System.out.println("Biến d kiểu số thực lớn, có giá trị là " + d);
        System.out.println("Biến b kiểu dữ liệu boolean, có giá trị là " + b);
        System.out.println("Biến c kiểu ký tự, có giá trị là " + c);
        System.out.println("Biên s kiểu chuỗi, có giá trị là " + s);
    }
}
